import { toast } from 'react-toastify';

// Success Login Toast
export const successLoginToast = () => {
  toast.success('Successfully Logged in', {
    position: 'top-right',
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: 'light',
  });
};

